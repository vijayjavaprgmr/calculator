package com.bnpp.kata;

public class Calculator {

	public Integer twoNumbersAddition(int i, int j) {
		return (i + j);
	}

	public Integer twoNumbersSubtraction(int i, int j) {
		return (i - j);
	}

	public Integer twoNumbersMultiplication(int i, int j) {
		return (i * j);
	}

	public Double twoNumbersDivision(double d, double e) {
		return (d / e);
	}

}